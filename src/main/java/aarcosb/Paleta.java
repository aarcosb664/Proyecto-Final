package aarcosb;

public class Paleta {
    private double pX;
    private double pY; 
    private double pW;
    private double pH;

    public Paleta() {
        pX = 450;
        pY = 20;
        pW = 100;
        pH = 10;
    }

    public double getpX() {
        return pX;
    }

    public double getpY() {
        return pY;
    }

    public double getpW() {
        return pW;
    }

    public double getpH() {
        return pH;
    }
}